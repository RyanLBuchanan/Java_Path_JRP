
public class Zoo {

		// City 
		String city;
		// Size
		int size;
		// Age
		double age;
		// Type of zoo (aquatic, reptile, general, etc.)
		String zooType;
		
		
		public Zoo(String city, int size, double age, String zooType) {
			super();
			this.city = city;
			this.size = size;
			this.age = age;
			this.zooType = zooType;
		}
		
		
		
	}
